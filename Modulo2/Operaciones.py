def Suma(sumando, sumador):
    suma = sumando + sumador
    return(suma)

def Resta(minuendo, sustraendo):
    resta = minuendo - sustraendo
    return(resta)

def Multiplicacion(factor1, factor2):
    multiplicacion = factor1 * factor2
    return(multiplicacion)

def Divicion(dividendo, divisor):
    divicion = dividendo / divisor
    return(divicion)